/*  
    面向对象的本质就是，编写代码时的所有操作都是通过对象来进行的。
    面向对象的编程，有两个步骤
        - 找对象
        - 使用对象的属性和方法

    学习对象：
        - 明确对象代表什么，有什么用
        - 如何获取对象
        - 如何使用对象（对象中的属性和方法）

    对象的分类：
        - 内建对象：由ES标准定义的对象（Object Function String Number...）
        - 宿主对象：由运行环境提供的对象（Window Document...）
        - 自定义对象：由开发人员自定义的对象
*/
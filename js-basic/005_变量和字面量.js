/*
    字面量其实就是一个值，它所代表的含义就是它字面的意思
    JS中所有的字面量都可以直接使用

    变量：可以用于“存储”字面量，变量存储的字面量可以随意修改
    通过变量可以对字面量进行描述，并且变量可以比较方便修改
*/
console.log(123)
console.log(true)
console.log('Hello')

let x = 80;
console.log(x);

let age = 80;
console.log(age);

/*
    多行注释：shift + alt + a
    变量的使用：
        声明变量 --> let 变量名1, 变量名2, 变量3; （有块作用域）/var 变量名; (老方式，无块作用域)
        变量赋值 --> a = <value>;
        声明和赋值同时进行 --> let 变量名1 = <value>, 变量名2 = <value>;
*/

let i = 0;
console.log(i);